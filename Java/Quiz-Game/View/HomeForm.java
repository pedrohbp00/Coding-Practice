    private void btnIniciarQuizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarQuizActionPerformed
        String quiz = null;
        
        if(rdbCR7.isSelected()){
            quiz = "CR7";
            } else if(rdbCS.isSelected()){
                quiz = "CS";
                } else if(rdbLOL.isSelected()){
                    quiz = "LOL";
                    } else if(rdbMATEMATICA.isSelected()){
                        quiz = "MATEMATICA";
                        } else if(rdbNARUTO.isSelected()){
                            quiz = "NARUTO";
                            } else if(rdbSUPERNATURAL.isSelected()){
                                quiz = "SUPERNATURAL";
                                } else {
                                    JOptionPane.showMessageDialog(null, "Selecione um quiz para iniciar!");
                                }
        QuizForm qf = new QuizForm(quiz);
        qf.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnIniciarQuizActionPerformed

